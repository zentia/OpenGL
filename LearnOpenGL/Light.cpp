Light::~Light()
{
	delete pShader;
	delete pMesh;
}

void Light::Update()
{
	pShader->use();
	pShader->setFloat3("objectColor", 1.0f, 0.5f, 0.3f);
	pShader->setFloat3("lightColor", 1.0f, 1.0f, 1.0f);
	pMesh->Update();
}